export function tFor(lang: Lang, key: string, ...args: any[]): string {
  const val = strings[lang]?.[key] ?? strings.en[key] ?? key;
  return typeof val === "function" ? val(...args) : (val as string);
}

export function isRTLFor(lang: Lang): boolean {
  return lang === "ar";
}
